# N = 32

# =====================================================================
# =====================================================================

# DATA

Fm=8000; Tm=1/Fm;
f1=1000; f2=400;
N=32;
T=(N-1)*Tm;
tp=[0:Tm:T];
Tc=(512-1)*Tm;
t=[tp,[tp(length(tp)):Tm:tp(length(tp))+Tc]];

# =====================================================================

# SIGNAL

xp=cos(2*pi*f1*tp)+cos(2*pi*f2*tp);
Xe=[xp,zeros(1,512)];

# DFT

[Xk,k]=dft(Xe,N+512);

# =====================================================================

# AMPLITUDE PLOT

subplot(3,2,1);
plot(t,Xe,'Linewidth',2,'b');
title('32 muestras de x(t) con 512 ceros');
xlabel('tiempo en segundos');
xlim([0,0.07]);

# PHASE PLOT

subplot(3,2,2);
plot(k,abs(Xk),'Linewidth',2,'b');
title('|X(f)| con N=32 mas 512 ceros'); xlabel('frecuencia en Hz');

