# N = 32

# =====================================================================
# =====================================================================

# DATA

Fm=8000; Tm=1/Fm;
N1=32;
T1=(N1-1)*Tm;
t1=[0:Tm:T1];
f1=1000; f2=400;

# =====================================================================

# SIGNAL

x1 = cos(2*pi*f1*t1)+cos(2*pi*f2*t1);

# DFT

[xk1,k1] = dft(x1,N1);
fk1 = k1*(Fm/N1);

# =====================================================================

# AMPLITUDE PLOT

subplot(3,2,1)
stem(fk1,abs(xk1),'b')
title("|X(f)| con N=32")
xlabel("frecuencia en Hz")

# PHASE PLOT

subplot(3,2,2)
stem(fk1,angle(xk1),'b')
title(" Fase X(f) con N=32")
xlabel("frecuencia en Hz")

